package practice.variablesPractice;

public class WholeNumbersPractice {
    public static void main(String[] args) {
        byte number1 = 9;
        short number2 = 23;
        int number3 = 2;
        long number4 = 123456789L;

        System.out.println("number 1 = "  +  "\"" + number1 + "\""); // long version escape sequence
        System.out.println("number 2 = \"" + number2 + "\"");
        System.out.print("number 3 = \"" + number3 + "\"\n");
        System.out.println("number 4 = \"" + number4 + "\"");



    }

}

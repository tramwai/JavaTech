package practice.loopsPractice;

public class ForiPractice2 {
    public static void main(String[] args) {

        for (int i = 1; i < 6 ; i++) {
            System.out.println("Number " + i );
        }

        // different starting point
        for (int i = 0; i < 5 ; i++) {
            System.out.println("Number " + (i + 1));
        }

    }
}

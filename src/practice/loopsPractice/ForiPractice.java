package practice.loopsPractice;

public class ForiPractice {
    public static void main(String[] args) {

        // run message 3 times
        for (int i = 0; i < 3; i++) {
            System.out.println("Congrats!!!");
            System.out.println("i = " + i);
        }

        for (int i = 1; i <= 3; i++) {
            System.out.println("Congrats!!!");
            System.out.println("i = " + i);
        }

        for (int i = 3; i > 0; i--) {
            System.out.println("Congrats!!!");
            System.out.println("i = " + i);
        }

        for (int i = 3; i >= 1; i--) {
            System.out.println("Congrats!!!");
            System.out.println("i = " + i);
        }
        for (int i = 100; i > 97; i--) {
            System.out.println("Congrats!!!");
            System.out.println("i = " + i);
        }
    }
}

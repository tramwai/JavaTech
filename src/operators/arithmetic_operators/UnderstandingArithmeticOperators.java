package operators.arithmetic_operators;

public class UnderstandingArithmeticOperators {
    public static void main(String[] args) {
        /*
        Find the result of the below expressions
        3 + 5 = 8
        5 - 2 = 3
        10 / 2 = 5
        2 * 4 = 8
        10 % 3 = 1
         */
        System.out.println(3 + 5);
        System.out.println(5 - 2);
        System.out.println(10 / 2);
        System.out.println(2 * 4);
        System.out.println(10 % 3);


        /*
        Create 2 int variables called num1 and num 2 and store
        6 and 4 in these variables and find
        sum
        subtraction
        product
        division
        remainder

         */
        int num1 = 6;
        int num2 = 4;
        System.out.println("The sum of numbers is = " + (num1 + num2));
        System.out.println("The difference is = " + (num1 - num2));
        System.out.println("The product is = " + num1 * num2);
        System.out.println("The division is =" + num1 / num2);
        System.out.println("The remainder is = " + num1 % num2);
        System.out.println(199/100);


    }
}

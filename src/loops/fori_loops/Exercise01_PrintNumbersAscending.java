package loops.fori_loops;

public class Exercise01_PrintNumbersAscending {
    public static void main(String[] args) {
        /*
        write a program to print numbers from 1 to 10(1 and 10 are included)
         */
        // start point: 1
        // end point: 10
        // update: increment

        for (int i = 1; i <= 10 ; i++) {
            System.out.println(i);
        }

    }


}

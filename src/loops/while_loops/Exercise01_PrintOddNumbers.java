package loops.while_loops;

public class Exercise01_PrintOddNumbers {
    public static void main(String[] args) {
        // print odd numbers from 1 to 10 with while loop
        int i = 1;
        while (i <= 10){
            if(i % 2 == 1) System.out.println(i);
            i++;
            }
        }

    }


package loops.while_loops;

public class UnderstandingWhileLoops {
    public static void main(String[] args) {
        //Syntax While(condition true)
        //              {execute this}
        // print numbers from 1 to 10
        int start = 1;
        while (start <= 10){
            System.out.println(start);
            start++;
        }
    }
}

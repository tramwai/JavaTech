package escape_sequences;

public class Concatenation {
    public static void main(String[] args) {
        System.out.println("Hello World");
        System.out.println("Hello " + "World");
        System.out.println("Hello" + " World");
        System.out.println();
        System.out.println("Hello" + " " + "World");
        System.out.println();
        System.out.println("Today is Sunday");

    }
}
